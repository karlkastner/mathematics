%% product of stacked 3x3 matrices
function C = mtimes3x3(A,B)
	C = [	A(1,1,:).*B(1,1,:) + A(1,2,:).*B(2,1,:) + A(1,3,:).*B(3,1,:), ...
		A(1,1,:).*B(1,2,:) + A(1,2,:).*B(2,2,:) + A(1,3,:).*B(3,2,:), ...
		A(1,1,:).*B(1,3,:) + A(1,2,:).*B(2,3,:) + A(1,3,:).*B(3,3,:);
		B(1,1,:).*A(2,1,:) + B(2,1,:).*A(2,2,:) + A(2,3,:).*B(3,1,:), ...
		B(1,2,:).*A(2,1,:) + A(2,2,:).*B(2,2,:) + A(2,3,:).*B(3,2,:), ...
		B(1,3,:).*A(2,1,:) + A(2,2,:).*B(2,3,:) + A(2,3,:).*B(3,3,:);
		B(1,1,:).*A(3,1,:) + B(2,1,:).*A(3,2,:) + B(3,1,:).*A(3,3,:), ...
		B(1,2,:).*A(3,1,:) + B(2,2,:).*A(3,2,:) + B(3,2,:).*A(3,3,:), ...
		B(1,3,:).*A(3,1,:) + B(2,3,:).*A(3,2,:) + A(3,3,:).*B(3,3,:)];
end

